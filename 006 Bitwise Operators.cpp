#include<iostream>
using namespace std;


int main(){
	
	int a=8,b=1;
	
	cout<<"AND operator: "<<(a&b)<<endl;
	
	cout<<"OR operator: "<<(a|b)<<endl;
	
	cout<<"XOR operator: "<<(a^b)<<endl;
	
	cout<<"LEFT Bitwise operator: "<<(a<<b)<<endl;
	
	cout<<"RIGHT Bitwise operator: "<<(a>>b)<<endl;
	
	return 0;
	
}
